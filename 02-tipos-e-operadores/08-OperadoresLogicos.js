/*
Booleans: operadores logincos
 - &&(and) retorna true se ambos os dados da comparação forem true
 - ||(or) retorna true se ao menos um dos dados forem true
 - !(not) inverte os valores, true vira false e vice versa
*/
console.log(true && true)
console.log(true && false)
console.log(true || true)
console.log(false || true)
console.log(!true)

console.log(2 > 1 && 3 > 4)
console.log(2 > 1 || 3 > 4)
console.log(!(2 > 1 || 3 > 4))