/*
Booleans: operador ternario
    - faz um comparativo em apenas uma unica linha
    - não é muito utilizado e pode deixar o codigo complicado de ler
    - condição ? 'retornar se true' : 'retornar se false'
*/
console.log(true ? 'retornar se true' : 'retornar se false')
console.log(false ? 'retornar se true' : 'retornar se false')
console.log(5 > 2 ? 'retornar se true' : 'retornar se false')
console.log(100 < 20 ? 'retornar se true' : 'retornar se false')